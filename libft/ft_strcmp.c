#include <string.h>


int	ft_strcmp(const char *s1, const char *s2)
{
	if ((s1 == NULL) || (s2 == NULL))
		return (0);
	while (*s1 == *s2)
	{
		if (*s1 == '\0')
			return (0);
		++s1;
		++s2;
	}
	return ((int)(*s1 - *s2));
}

